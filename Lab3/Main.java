public class Main {
    public static void main(String[] args) {
        System.out.println("=== –ì—Ä–∞ –•—Ä–µ—Å—Ç–∏–∫–∏-–ù–æ–ª–∏–∫–∏ ===");

        // –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –æ–±'—î–∫—Ç—ñ–≤ –≥—Ä–∞–≤—Ü—ñ–≤
        Player player1 = new Player("–ì—Ä–∞–≤–µ—Ü—å 1", 'X');
        Player player2 = new Player("–ì—Ä–∞–≤–µ—Ü—å 2", 'O');

        // –°—Ç–≤–æ—Ä–µ–Ω–Ω—è —ñ–≥—Ä–æ–≤–æ–≥–æ –ø–æ–ª—è
        GameBoard board = new GameBoard();

        // –ó–º—ñ–Ω–Ω–∞ –¥–ª—è –≤—ñ–¥—Å—Ç–µ–∂–µ–Ω–Ω—è —á–µ—Ä–≥–∏
        Player currentPlayer = player1;

        while (true) {
            board.printBoard();
            int[] move = currentPlayer.makeMove();
            if (!board.makeMove(move[0], move[1], currentPlayer.getSymbol())) {
                continue; // —è–∫—â–æ —Ö—ñ–¥ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∏–π ‚Äî —Å–ø—Ä–æ–±—É–≤–∞—Ç–∏ –∑–Ω–æ–≤—É
            }

            // –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –ø–µ—Ä–µ–º–æ–≥–∏
            if (board.checkWin(currentPlayer.getSymbol())) {
                board.printBoard();
                System.out.println("üéâ –ü–µ—Ä–µ–º—ñ–≥ " + currentPlayer.getName() + "!");
                break;
            }

            // –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –Ω—ñ—á–∏—ó
            if (board.isFull()) {
                board.printBoard();
                System.out.println("ü§ù –ù—ñ—á–∏—è!");
                break;
            }

            // –ó–º—ñ–Ω–∞ –≥—Ä–∞–≤—Ü—è
            currentPlayer = (currentPlayer == player1) ? player2 : player1;
        }
    }
}
